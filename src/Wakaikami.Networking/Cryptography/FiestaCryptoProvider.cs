    public void SetXorKeyPositionToRandom()
    {
        XorPosition = (ushort)Random.Shared.Next(0, _xorTable.Length);
    }

    public FiestaCryptoProvider()
    {
        SetXorKeyPositionToRandom();
    }

    public void Crypt(Span<byte> data)
    {
        foreach (ref var b in data)
        {
            b ^= _xorTable[XorPosition];

            ++XorPosition;

            if (XorPosition >= _xorTable.Length)
            {
                XorPosition = 0;
            }
        }
    }
}
